# выбор окончания в зависимости от количества программистов (0<=n<=1000)
n = int(input('Введите количество программистов \n'))
w = 'программист'
if ((n % 10) == 1) and ((n % 100) != 11):
    print(n, ' ', w, sep='')
elif (2 <= (n % 10) <= 4) and ((12 > (n % 100)) or ((n % 100) > 14)):
    print(n, ' ', w, 'а', sep='')
else:
    print(n, ' ', w, 'ов', sep='')
